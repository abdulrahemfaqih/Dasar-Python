# cara membuat string

'''
    1. menggunakan single quote '....'
'''
data = 'abdul rahem faqih'
print(data)

'''
    2. menggunakan double  quote "...."
'''
data = "abdul rahem faqih"
print(data)

'''
    3. menggunakan dua duanya "'....'"
'''
data = "ini adalah hari jum'at"
print(data)

